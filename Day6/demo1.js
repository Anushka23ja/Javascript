//Function Expression 
var myfun1 = function show(){
    console.log("It is a function Expression");
}

//Anonymous Function
var myfun2 = function(){
    console.log("It is a Anonymous Functions");
}

//Arrow Function
var myfun3 = () => {
    console.log("It is a Arrow Function");
}

myfun1();
myfun2();
myfun3();